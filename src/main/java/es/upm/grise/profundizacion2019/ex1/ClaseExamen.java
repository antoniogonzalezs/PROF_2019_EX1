public class ClaseExamen {

	//Nodo 1
	public float metodoAProbar(int a, int b) {

		float result = 0;

		//Nodo 2
		while( a > b) {
			//Nodo 3
			a--;
		}

		//Nodo 4
		if (a <= b) {
			//Nodo 5
			result = 0;
		}
		else {
			//Nodo 6
			result = b;
		}

		//Nodo 7
		return result;
	}
}
